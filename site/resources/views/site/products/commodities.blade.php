                <h3>WHY COMMODITY TRADING?</h3>
                <p>Commodity trading can be done for various purposes out of which two are most important Against the exposure towards physical trades -for risk management (Hedging) Having no exposure in physical form - Investment & Speculation Hedging is the practice of offsetting the price risk inherent in any cash market position by taking an opposite position in the futures market. A long hedge involves buying futures contracts to protect against possible increasing prices of commodities. A short hedge involves selling futures contracts to protect against possible declining prices of commodities. Speculation is the practice of engaging in risky financial transactions in an attempt to profit from short or medium term fluctuations in the market value of a tradable good such as a financial instrument excommodity</p>
